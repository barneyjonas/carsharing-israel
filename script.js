// ── State ──────────────────────────────────────────────────────────────────
let providers = [];
let lang = 'he';
let filters = { city: 'all', type: 'all', vehicle: 'all' };
let map = null;
let markers = [];

// ── Boot ──────────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', async () => {
  await loadProviders();
  applyLang();
  renderProviders();
  initMap();
  renderTable();
  bindEvents();
});

async function loadProviders() {
  try {
    const res = await fetch('providers.json');
    providers = await res.json();
  } catch (e) {
    console.error('Failed to load providers:', e);
    providers = [];
  }
}

// ── Language ──────────────────────────────────────────────────────────────
function applyLang() {
  const t = i18n[lang];
  document.documentElement.lang = lang === 'he' ? 'he' : 'en';
  document.body.classList.toggle('lang-en', lang === 'en');
  document.body.classList.toggle('lang-he', lang === 'he');

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.querySelector('.lang-toggle').textContent = lang === 'he' ? 'EN' : 'עב';

  renderProviders();
  renderTable();
  updateMapPopups();
}

function toggleLang() {
  lang = lang === 'he' ? 'en' : 'he';
  applyLang();
}

// ── Filters ───────────────────────────────────────────────────────────────
function getFilteredProviders() {
  return providers.filter(p => {
    if (filters.city !== 'all' && !p.cities.includes(filters.city)) return false;
    if (filters.type !== 'all' && p.type !== filters.type) return false;
    if (filters.vehicle !== 'all' && !p.vehicles.includes(filters.vehicle)) return false;
    return true;
  });
}

function resetFilters() {
  filters = { city: 'all', type: 'all', vehicle: 'all' };
  document.getElementById('filter-city').value = 'all';
  document.getElementById('filter-type').value = 'all';
  document.getElementById('filter-vehicle').value = 'all';
  renderProviders();
  updateMapMarkers();
}

// ── Provider Cards ────────────────────────────────────────────────────────
function getTypeLabel(type) {
  const map = {
    'free-floating': i18n[lang].typeFreeFloating,
    'round-trip': i18n[lang].typeRoundTrip,
    'peer-to-peer': i18n[lang].typePeerToPeer
  };
  return map[type] || type;
}

function getCityLabel(city) {
  const map = {
    'tel-aviv': i18n[lang].cityTelAviv,
    'jerusalem': i18n[lang].cityJerusalem,
    'haifa': i18n[lang].cityHaifa,
    'beer-sheva': i18n[lang].cityBeerSheva,
    'ramat-gan': lang === 'he' ? 'רמת גן' : 'Ramat Gan',
    'givatayim': lang === 'he' ? 'גבעתיים' : 'Givatayim',
    'petah-tikva': lang === 'he' ? 'פתח תקווה' : 'Petah Tikva'
  };
  return map[city] || city;
}

function getVehicleLabel(v) {
  const map = {
    'electric': i18n[lang].vehicleElectric,
    'hybrid': i18n[lang].vehicleHybrid,
    'standard': i18n[lang].vehicleStandard
  };
  return map[v] || v;
}

function renderProviders() {
  const grid = document.getElementById('providers-grid');
  if (!grid) return;

  const filtered = getFilteredProviders();
  const t = i18n[lang];

  if (filtered.length === 0) {
    grid.innerHTML = `<div class="no-results">${t.noResults}</div>`;
    return;
  }

  grid.innerHTML = filtered.map(p => {
    const name = p.name[lang] || p.name.he;
    const desc = p.description[lang] || p.description.he;
    const price = p.priceInfo[lang] || p.priceInfo.he;
    const logoEl = p.active && p.logo && !p.logo.includes('placeholder')
      ? `<img class="card-logo" src="${p.logo}" alt="${name}" onerror="this.style.display='none'">`
      : `<div class="card-logo-placeholder">🚗</div>`;

    const cityTags = p.cities.map(c => `<span class="tag">${getCityLabel(c)}</span>`).join('');
    const vehicleTags = p.vehicles.map(v => `<span class="tag">${getVehicleLabel(v)}</span>`).join('');

    const siteBtn = p.active && p.website !== '#'
      ? `<a href="${p.website}" target="_blank" rel="noopener" class="card-link">${t.visitSite}</a>`
      : `<span class="card-link disabled">${t.soonAvailable}</span>`;

    const appLinks = (p.appLinks?.ios || p.appLinks?.android) ? `
      <div class="app-links">
        ${p.appLinks.ios ? `<a href="${p.appLinks.ios}" class="app-link" title="iOS">🍎</a>` : ''}
        ${p.appLinks.android ? `<a href="${p.appLinks.android}" class="app-link" title="Android">🤖</a>` : ''}
      </div>` : '';

    return `
      <div class="provider-card ${p.active ? '' : 'inactive'}" data-id="${p.id}">
        <div class="card-header">
          ${logoEl}
          <div>
            <div class="card-name">${name}</div>
            <span class="card-type type-${p.type}">${getTypeLabel(p.type)}</span>
          </div>
        </div>
        <div class="card-body">
          <p class="card-description">${desc}</p>
          <div class="card-tags">${cityTags}${vehicleTags}</div>
          <div class="card-price">${price}</div>
        </div>
        <div class="card-footer">
          ${siteBtn}
          ${appLinks}
        </div>
      </div>`;
  }).join('');
}

// ── Map ───────────────────────────────────────────────────────────────────
function initMap() {
  if (!window.L) return;

  map = L.map('map').setView([31.8, 34.9], 8);

  L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    attribution: '© <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a>',
    maxZoom: 18
  }).addTo(map);

  const icon = L.divIcon({
    className: '',
    html: `<div style="background:#FF0000;width:14px;height:14px;border-radius:50%;border:2px solid white;box-shadow:0 1px 4px rgba(0,0,0,0.4)"></div>`,
    iconSize: [14, 14],
    iconAnchor: [7, 7]
  });

  providers.forEach(p => {
    if (!p.coordinates) return;
    const name = p.name[lang] || p.name.he;
    const marker = L.marker(p.coordinates, { icon })
      .bindPopup(`<strong>${name}</strong><br>${getTypeLabel(p.type)}`)
      .addTo(map);
    markers.push({ id: p.id, marker });
  });
}

function updateMapMarkers() {
  if (!map) return;
  const filtered = getFilteredProviders().map(p => p.id);
  markers.forEach(({ id, marker }) => {
    if (filtered.includes(id)) {
      marker.addTo(map);
    } else {
      map.removeLayer(marker);
    }
  });
}

function updateMapPopups() {
  if (!map) return;
  markers.forEach(({ id, marker }) => {
    const p = providers.find(x => x.id === id);
    if (!p) return;
    const name = p.name[lang] || p.name.he;
    marker.setPopupContent(`<strong>${name}</strong><br>${getTypeLabel(p.type)}`);
  });
}

// ── Comparison Table ──────────────────────────────────────────────────────
function renderTable() {
  const tbody = document.getElementById('compare-tbody');
  const thead = document.getElementById('compare-thead');
  if (!tbody || !thead) return;

  const t = i18n[lang];

  thead.innerHTML = `<tr>
    <th>${t.colProvider}</th>
    <th>${t.colType}</th>
    <th>${t.colCities}</th>
    <th>${t.colElectric}</th>
    <th>${t.colApp}</th>
    <th>${t.colActive}</th>
  </tr>`;

  tbody.innerHTML = providers.map(p => {
    const name = p.name[lang] || p.name.he;
    const cities = p.cities.map(c => getCityLabel(c)).join(', ');
    const hasElectric = p.vehicles.includes('electric') ? '✓' : '✗';
    const hasApp = p.appLinks?.ios || p.appLinks?.android ? '✓' : '✗';
    const active = p.active ? '✓' : '✗';
    return `<tr>
      <td class="highlight">${name}</td>
      <td>${getTypeLabel(p.type)}</td>
      <td>${cities}</td>
      <td class="${p.vehicles.includes('electric') ? 'check' : 'cross'}">${hasElectric}</td>
      <td class="${(p.appLinks?.ios || p.appLinks?.android) ? 'check' : 'cross'}">${hasApp}</td>
      <td class="${p.active ? 'check' : 'cross'}">${active}</td>
    </tr>`;
  }).join('');
}

// ── Events ────────────────────────────────────────────────────────────────
function bindEvents() {
  document.querySelector('.lang-toggle')?.addEventListener('click', toggleLang);

  document.getElementById('filter-city')?.addEventListener('change', e => {
    filters.city = e.target.value;
    renderProviders();
    updateMapMarkers();
  });

  document.getElementById('filter-type')?.addEventListener('change', e => {
    filters.type = e.target.value;
    renderProviders();
    updateMapMarkers();
  });

  document.getElementById('filter-vehicle')?.addEventListener('change', e => {
    filters.vehicle = e.target.value;
    renderProviders();
    updateMapMarkers();
  });

  document.getElementById('filter-reset')?.addEventListener('click', resetFilters);
}
